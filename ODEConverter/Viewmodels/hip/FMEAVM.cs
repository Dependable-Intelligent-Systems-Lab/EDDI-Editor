using ODELib.hip;
using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using Xceed.Wpf.Toolkit.PropertyGrid.Attributes;

namespace ODEConverter.Viewmodels.hip
{
    public class FMEAVM
    {
        /*****************************************************************************************************/
        /* Enums/Constants
        /*****************************************************************************************************/
        #region Constants
        #endregion Constants

        /*****************************************************************************************************/
        /* Data
        /*****************************************************************************************************/
        #region Data
        #endregion Data

        /*****************************************************************************************************/
        /* Constructors
        /*****************************************************************************************************/
        #region Constructors

        public FMEAVM(FMEA fmea)
        {
            HipFMEA = fmea;

            foreach (var fmeacomp in fmea.Components)
            {
                var vm = new FMEAComponentVM(fmeacomp);
                Components.Add(vm);
            }
        }

        #endregion Constructors

        /*****************************************************************************************************/
        /* Properties
        /*****************************************************************************************************/
        #region Properties

        private ODELib.hip.FMEA HipFMEA { get; set; }

        //----------------------------------------------------------------------------------------------------//
        //----------------------------------------------------------------------------------------------------//

        [ExpandableObject]
        public ExpandableList Components { get; set; } = new ExpandableList();

        //----------------------------------------------------------------------------------------------------//

        public bool IsExpanded { get; set; }

        //----------------------------------------------------------------------------------------------------//


        #endregion Properties

        /*****************************************************************************************************/
        /* Functions
        /*****************************************************************************************************/
        #region Functions
        #endregion Functions

    }
}
